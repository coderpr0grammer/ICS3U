import java.io.*;

class Vector {
    private String name;
    private int age;
    private String occupation;
    
    public Vector(String name, int age, String occupation) {
       this.name = name;
       this.age = age;
       this.occupation = occupation;
    }
    
    public String getName() {
       return this.name;
    }
    
    public int getAge() {
       return this.age;
    }
    
    public String getOccupation() {
       return this.occupation;
    }
 }

